#!/bin/bash

# ================================================================================
# 运行训练和测试脚本
# 用法: bash run_train_and_test.sh
# ================================================================================

set -e  # 遇到错误立即退出

echo "================================================================================"
echo "开始运行 AgentCF 训练和测试流程"
echo "================================================================================"

# 记录开始时间
START_TIME=$(date +%s)

# ========== 步骤1: 运行训练 ==========
echo ""
echo ">>> [步骤 1/2] 运行训练脚本: AgentCF_train_check.py"
echo "--------------------------------------------------------------------------------"

python AgentCF_train_check.py

if [ $? -eq 0 ]; then
    echo "✅ 训练完成"
else
    echo "❌ 训练失败，退出"
    exit 1
fi

# ========== 步骤2: 运行测试 ==========
echo ""
echo ">>> [步骤 2/2] 运行测试脚本: AgentCF_Test_log-.py"
echo "--------------------------------------------------------------------------------"

python AgentCF_Test_log-.py

if [ $? -eq 0 ]; then
    echo "✅ 测试完成"
else
    echo "❌ 测试失败，退出"
    exit 1
fi

# ========== 完成 ==========
END_TIME=$(date +%s)
ELAPSED=$((END_TIME - START_TIME))

echo ""
echo "================================================================================"
echo "✅ 全部完成！总耗时: ${ELAPSED} 秒"
echo "================================================================================"